<h2>¿Qué son los CFP <strong>públicos</strong>? </h2>

							<p>Los Centro de Formación Profesional (CFP) son escuelas públicas de la ciudad de Buenos Aires que capacitan gratuitamente a jóvenes y adultos en oficios. Basados en trayectos formativos de alrededor de dos año, los CFP capacitan en construcción, electricidad, plomería y gas, gastronomía, peluquería y estética, informática y programación, mecánica automotriz, energía renovables, entre muchas otras áreas. </p>
<p>La transmisión de los saberes y reglas de cada oficios –actualizada en nuestras escuelas mediante el uso de tecnologías y herramientas digitales– hunde sus raíces en la relación entre el maestro artesano y el aprendiz. En nuestro país, esta función pedagógica se institucionalizó a mediados del siglo anterior, de la mano de proyectos de tinte industrialista, primero, y desarrollistas, después. De ese recorrido se nutren nuestras instituciones educativas, caracterizadas en el presente por la calidad y diversidad de sus cursos y por sus activas comunidades educativas.</p>

